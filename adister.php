 <h5><a href="">  II. Бағдарламалық қамтамасыз етуді тексеру әдістері</a> </h5>
<ul>
<li>
Функционалдық тестілеу</li>
<li>Өнімділік пен жүктемені сынау
<li>Қауіпсіздік сынағы
<li>Үйлесімділік сынағы
<li>Қол жетімділікті тексеру
<li>Қолдану мүмкіндігін тексеру
</li></Ul>
<p>
Марк бағдарламалық жасақтаманы тестілеу әдістерін тестілеу мақсаттары мен бағдарламаға қойылатын талаптарға байланысты таңдау керек екенін түсінді. Функционалдық тестілеу, мысалы, бағдарламаның оның функционалдық талаптарына сәйкес келетінін тексеруге мүмкіндік береді, ал өнімділік пен жүктемені тексеру бағдарламаның үлкен көлемдегі деректермен немесе сұраулармен қалай жұмыс істейтінін тексеруге мүмкіндік береді.

Қауіпсіздік сынағы шабуылдаушылар пайдалана алатын бағдарламаның осал тұстарын тексереді, ал үйлесімділік сынағы бағдарламаның әртүрлі платформаларда және әртүрлі орталарда қалай жұмыс істейтінін тексеруге мүмкіндік береді.

Қолжетімділік пен ыңғайлылықты тексеру бағдарламаның пайдаланушыға ыңғайлы және әртүрлі қажеттіліктері бар пайдаланушылар үшін қолжетімді болуын қамтамасыз ету үшін маңызды. Ол сондай-ақ пайдаланушы интерфейсіне немесе бағдарлама функционалдығына қатысты мәселелерді анықтауға көмектеседі.

Марк бағдарламалық қамтамасыз етуді тестілеу бағдарламалық жасақтаманың өмірлік циклінің барлық кезеңдерінде, бағдарламалық қамтамасыз етуді шығарғаннан кейін жоспарлаудан бастап тестілеуге дейін жүргізілуі керек екенін түсінді. Сондай-ақ ол тестілеуді автоматтандыру уақытты үнемдеуге және тестілеу сапасын жақсартуға көмектесетінін түсінді.

Бағдарламалық жасақтаманы тестілеу саласындағы білімі мен дағдыларының арқасында Марк табысты бағдарламалық жасақтама әзірлеушісі болды және басқа бағдарламашыларға бағдарламалық жасақтаманы тестілеудің маңыздылығы мен оны қалай дұрыс орындау керектігін үйретті. Оның тәжірибесі көрсеткендей, бағдарламалық қамтамасыз етуді тестілеу қатесіз жұмыс істейтін және пайдаланушылардың қажеттіліктерін қанағаттандыратын бағдарламалық қамтамасыз етуді жасау үшін қажет.
